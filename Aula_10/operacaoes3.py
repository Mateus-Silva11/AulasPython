def soma(n1,n2):
    r = n1 + n2
    return r

def subtracao(n1,n2):
    r= n1 - n2
    return r

def multiplicacao(n1,n2):
    r = n1 * n2
    return r

def divisao_inteira(n1,n2):
    r = n1 // n2
    return r

def divisao_fracionada(n1,n2):
    r = n1 / n2
    return r

def resto(n1,n2):
    r = n1 % n2
    return r

def raiz(n1,n2):    
    r =  n1 **(1/n2)
    return r

          